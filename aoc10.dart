calc1(String s) {
  var x = parse(s);
  var corrupts = x.map((l) {
    var stack = [];
    for (var i = 0; i < l.length; i++) {
      var c = l[i];
      if (chars.indexWhere((element) => element.item1 == c) != -1) {
        stack.add(c);
        continue;
      }
      var char = chars.firstWhereOrNull((element) => element.item2 == c);
      if (char != null) {
        if (stack.last == char.item1) {
          stack.removeLast();
        } else {
          return char;
        }
      }
    }
  });
  var counts = {};
  for (var c in corrupts) {
    if (c != null) {
      counts[c.item3] = (counts[c.item3] ?? 0) + 1;
    }
  }
  return counts.entries.map((e) => e.key * e.value).reduce((a, b) => a + b);
}

calc2(String s) {
  var x = parse(s);
  var incompletes = x.map((l) {
    var stack = [];
    for (var i = 0; i < l.length; i++) {
      var c = l[i];
      var char1 = chars.firstWhereOrNull((element) => element.item1 == c);
      if (char1 != null) {
        stack.add(char1);
        continue;
      }
      var char2 = chars.firstWhereOrNull((element) => element.item2 == c);
      if (char2 != null) {
        if (stack.last == char2) {
          stack.removeLast();
        } else {
          return null;
        }
      }
    }
    return List.from(stack.reversed);
  }).toList();
  var scores = incompletes.where((i) => i != null).map((i) {
    var score = 0;
    for (var c in i!) {
      score *= 5;
      score += c.item4 as int;
    }
    return score;
  }).toList();
  scores.sort();
  return scores[(scores.length / 2).floor()];
}

